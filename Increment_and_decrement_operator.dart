//++ increment operator
// -- decrement operator
void main(List<String> args) {
  var a = 10;
  var b = 20;

// a++ post increment

  print(a++); // it increase but did not display
  print(a);

// ++a pre increment
  print(++a);

// b-- post decrement

  print(b--);
  print(b);

// --b  pre decrement

  print(--b);
}
