void main(List<String> args) {
  var a = 10;
  var b = 20;
  print("$a + $b = ${a+b} "); // addition
  print("$a - $b = ${a-b} "); // subration
  print("$a * $b = ${a*b} "); // multiplication
  print("$a % $b = ${a%b} "); // remainder
  print("$a / $b = ${a/b} "); // divsion
  print("$a ~/ $b = ${a~/b} "); // floor division
}
